    private void btnCapNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhapActionPerformed
        // TODO add your handling code here:
        // Khai bao 1 doi tuong thi sinh
        ThiSinh objTS = new ThiSinh();
        
        String strNgaySinh = "", strDiemToan = "", strDiemLy = "", strDiemHoa = "";
        float diemToan = 0, diemLy = 0, diemHoa = 0;
        
        // Lay thong tin tren giao dien
        objTS.setSoBaoDanh(txtSoBaoDanh.getText());
        objTS.setHoTen(txtHoTen.getText());
        
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yy");
        strNgaySinh = dateFormat.format(jDateChooserNgaySinh.getDate());
        objTS.setNgaySinh(strNgaySinh);
        
        objTS.setDiaChi(txtDiaChi.getText());
        strDiemToan = txtDiemToan.getText();
        strDiemLy = txtDiemLy.getText();
        strDiemHoa = txtDiemHoa.getText();
        try {
            diemToan = Float.parseFloat(strDiemToan);
            diemLy = Float.parseFloat(strDiemLy);
            diemHoa = Float.parseFloat(strDiemHoa);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ban can nhap vao so nguyen. Chi tiet loi:" + e.getMessage());
            return;
        }
        objTS.setDiemToan(diemToan);
        objTS.setDiemLy(diemLy);
        objTS.setDiemHoa(diemHoa);
        
        if (cmbGioiTinh.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Ban can nhap chon gioi tinh");
            return;
        }
        else if (cmbGioiTinh.getSelectedIndex() == 1)
        {
            objTS.setGioiTinh("Nam");
        }
        else
        {
            objTS.setGioiTinh("Nu");
        }
        
        boolean ketQuaCapNhap = false;
        // Truong hop sua
        if (!soBaoDanh.isEmpty())
        {
            // Goi ham sua
            ketQuaCapNhap = DataProvider.QLThiSinh().capNhapThiSinh(objTS);
        }
        else
        {
            ketQuaCapNhap = DataProvider.QLThiSinh().themThiSinh(objTS);
        }
        
        if (ketQuaCapNhap == true)
        {
            JOptionPane.showMessageDialog(rootPane, "Cap nhap thong tin sinh vien thanh cong");
        }
        formDanhSachThiSinh.hienThiDanhSachThiSinh();
    }//GEN-LAST:event_btnCapNhapActionPerformed

