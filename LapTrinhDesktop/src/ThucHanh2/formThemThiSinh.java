    private void hienThiChiTietThiSinh() {
        // Lay thong tin chi tiet sinh vien
        ThiSinh objTS = DataProvider.QLThiSinh().timKiemThiSinhTheoSoBaoDanh(soBaoDanh);
        if (objTS != null)
        {
            // Hien thi len giao dien
            txtSoBaoDanh.setText(soBaoDanh);
            txtSoBaoDanh.setEnabled(false); // Khong cho doi tuong nhap vao
            txtHoTen.setText(objTS.getHoTen());
            
            DateFormat dateFormat = new SimpleDateFormat("dd/MM/yy");
            try {
                jDateChooserNgaySinh.setDate(dateFormat.parse(objTS.getNgaySinh()));
            } catch (ParseException ex) {
                System.err.println("Co loi chuyen doi ngay thang xay ra");
            }
            txtDiaChi.setText(objTS.getDiaChi());
            txtDiemToan.setText((objTS.getDiemToan()) + "");
            txtDiemLy.setText((objTS.getDiemLy()) + "");
            txtDiemHoa.setText((objTS.getDiemHoa()) + "");
            if (objTS.getGioiTinh().equals("Nam"))
            {
                cmbGioiTinh.setSelectedIndex(1);
            }
            else if (objTS.getGioiTinh().equals("Nu"))
            {
                cmbGioiTinh.setSelectedIndex(2);
            }
        }
    }

