    public static void hienThiDanhSachThiSinh()
    {
        // Khai bao 1 mang chua tieu de
        String tieuDe[] = new String[]{"Ma SV", "Ho ten", "Gioi tinh", "Ngay sinh", "Dia chi", "Tong diem", "Diem Toan", "Diem Ly", "Diem Hoa"};
        
        // Khai bao 1 doi tuong de dua du lieu len table
        DefaultTableModel model = new DefaultTableModel(tieuDe, 0);
        
        // Lay danh sach
        List<ThiSinh> lstSV = DataProvider.QLThiSinh().layDanhSachThiSinh();
        
        // Duyet danh sach de dua vao model
        Object row[];
        for (ThiSinh ts : lstSV)
        {
            row = new Object[9];
            row[0] = ts.getSoBaoDanh();
            row[1] = ts.getHoTen();
            row[2] = ts.getGioiTinh();
            row[3] = ts.getNgaySinh();
            row[4] = ts.getDiaChi();
            row[5] = ts.getTongDiem();
            row[6] = ts.getDiemToan();
            row[7] = ts.getDiemLy();
            row[8] = ts.getDiemHoa();
            
            // Them vao model
            model.addRow(row);
        }
        
        // Hien thi du lieu len table
        tblThiSinh.setModel(model);
    }
    
