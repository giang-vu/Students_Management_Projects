    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int ketQua = JOptionPane.showConfirmDialog(rootPane,
                "Ban co chac chan muon xoa thi sinh nay khong?",
                "Thong bao", JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.WARNING_MESSAGE);
        
        if (ketQua == JOptionPane.YES_OPTION)
        {
            // Xu ly xoa
            String soBaoDanh = "";
            
            // Lay thong tin ma sinh vien nguoi dung chon tren table
            int dongChon = tblThiSinh.getSelectedRow();
            soBaoDanh = "" + tblThiSinh.getValueAt(dongChon, 0);

            boolean ketQuaXoa = DataProvider.QLThiSinh().xoaThiSinh(soBaoDanh);
            if (ketQuaXoa == true) {
                JOptionPane.showMessageDialog(rootPane, "Xoa thong tin sinh vien thanh cong");
            }
        }
        formDanhSachThiSinh.hienThiDanhSachThiSinh();
    }//GEN-LAST:event_btnXoaActionPerformed

