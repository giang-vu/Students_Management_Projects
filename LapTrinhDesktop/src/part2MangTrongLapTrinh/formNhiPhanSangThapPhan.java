    private void btnChuyenDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChuyenDoiActionPerformed
        String strSoNhiPhan = "";
        int soThapPhan = 0;
        
        strSoNhiPhan = "" + txtNhapSo.getText();
        
        if (strSoNhiPhan.length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Ban can nhap vao so nhi phan");
            txtNhapSo.requestFocus();
            return;
        }
        
        // Chuyen so nhi phan ve mang ky tu chua cac bit
        char a[] = strSoNhiPhan.toCharArray();
        
        // Kiem tra mang chi chua 0 va 1
        for (int i = 0; i < a.length; i++)
        {
            if (a[i] != '0' && a[i] != '1')
            {
                JOptionPane.showMessageDialog(rootPane, "Ban can nhap vao so nhi phan");
                txtNhapSo.requestFocus();
                return;
            }
        }
        
        for (int i = 0; i < a.length; i++)
        {
            if (a[i] == '1')
            {
                soThapPhan += Math.pow(2, a.length - 1 - i);
            }
        }
        txtKetQua.setText("" + soThapPhan);
    }//GEN-LAST:event_btnChuyenDoiActionPerformed

