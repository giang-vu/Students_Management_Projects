    private void hienThiChiTietSinhVien() {
        // Lay thong tin chi tiet sinh vien
        QuanLySinhVien quanLySinhVien = new QuanLySinhVien();
        SinhVien objSV = quanLySinhVien.timKiemTheoMaSV(maSV);
        if (objSV != null) {
            // Hien thi len giao dien
            txtMaSV.setText(maSV);
            txtMaSV.setEnabled(false); // Khong cho doi tuong nhap vao
            txtHoTen.setText(objSV.getHoTen());
            jDateChooserNgaySinh.setDate(objSV.getNgaySinh());
            txtDienThoai.setText(objSV.getDienThoai());
            txtEmail.setText(objSV.getEmail());
            txtSoCMT.setText(objSV.getSoCMT());
            txtDiaChi.setText(objSV.getDiaChi());
            if (objSV.getGioiTinh() == true) // Neu la nu
            {
                radNu.setSelected(true);
            } else {
                radNam.setSelected(true);
            }
            // Lay doi tuong chuyen khoa
            QuanLyChuyenKhoa quanLyChuyenKhoa = new QuanLyChuyenKhoa();
            ChuyenKhoa objKhoa = quanLyChuyenKhoa.timKiemKhoaTheoMa(objSV.getMaKhoa());
            cmbChuyenKhoa.getModel().setSelectedItem(objKhoa);
        }
    }

    private void hienThiDanhSachKhoa() {
        // Khai bao 1 doi tuong
        QuanLyChuyenKhoa quanLyChuyenKhoa = new QuanLyChuyenKhoa();

        // Lay danh sach khoa
        List<ChuyenKhoa> lstKhoa = quanLyChuyenKhoa.layDanhSachKhoa();

        DefaultComboBoxModel model = new DefaultComboBoxModel();

        // Duyet danh sach chuyen khoa
        for (ChuyenKhoa ck : lstKhoa) {
            // Them vao model
            model.addElement(ck);
        }

        // Hien thi len danh sach chuyen khoa
        cmbChuyenKhoa.setModel(model);

        // Set lai hien thi cho combobox
        cmbChuyenKhoa.setRenderer(new ChuyenKhoaRenderer());
    }

    class ChuyenKhoaRenderer extends BasicComboBoxRenderer {

        @Override
        public Component getListCellRendererComponent(
                JList list, Object value, int index,
                boolean isSelected, boolean cellHasFocus) {
            super.getListCellRendererComponent(list, value, index,
                    isSelected, cellHasFocus);
            if (value != null) {
                ChuyenKhoa item = (ChuyenKhoa) value;
                setText(item.getTenKhoa());
            }

            return this;
        }
    }
