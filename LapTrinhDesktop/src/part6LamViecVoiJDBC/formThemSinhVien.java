    private void btnCapNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhapActionPerformed
        // TODO add your handling code here:

        // Khai bao 1 doi tuong
        SinhVien objSV = new SinhVien();

        // Lay thong tin tren giao dien
        objSV.setMaSV(txtMaSV.getText());
        objSV.setHoTen(txtHoTen.getText());
        objSV.setNgaySinh(jDateChooserNgaySinh.getDate());
        objSV.setDienThoai(txtDienThoai.getText());
        objSV.setEmail(txtEmail.getText());
        objSV.setSoCMT(txtSoCMT.getText());
        objSV.setDiaChi(txtDiaChi.getText());

        if (radNu.isSelected()) {
            objSV.setGioiTinh(true);
        } else {
            objSV.setGioiTinh(false);
        }

        // Khai bao ma khoa
        String maKhoa = "";

        // Lay doi tuong khoa duoc chon
        ChuyenKhoa objKhoa = (ChuyenKhoa) cmbChuyenKhoa.getSelectedItem();

        if (objKhoa != null) {
            maKhoa = objKhoa.getMaKhoa();
        }
        objSV.setMaKhoa(maKhoa);

        boolean ketQuaCapNhap = false;
        // Khai bao doi tuong 
        QuanLySinhVien quanLySinhVien = new QuanLySinhVien();
        // Truong hop sua
        if (!maSV.isEmpty()) {
            // Goi ham sua
            ketQuaCapNhap = quanLySinhVien.capNhapSinhVien(objSV);
        } else {
            ketQuaCapNhap = quanLySinhVien.themSinhVien(objSV);
        }

        if (ketQuaCapNhap == true) {
            JOptionPane.showMessageDialog(rootPane, "Cap nhap thong tin sinh vien thanh cong");
        }
        formDanhSachSinhVien.hienThiDanhSachSinhVien();
    }//GEN-LAST:event_btnCapNhapActionPerformed

