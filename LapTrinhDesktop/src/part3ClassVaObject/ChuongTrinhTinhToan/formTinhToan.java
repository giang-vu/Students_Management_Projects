    private void btnTinhToanHinhChuNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhToanHinhChuNhatActionPerformed
        // TODO add your handling code here:
        String strChieuDai = "", strChieuRong = "", strChuVi = "", strDienTich = "";
        double chieuDai = 0, chieuRong = 0;
        
        strChieuDai = txtChieuDai.getText();
        strChieuRong = txtChieuRong.getText();
        
        try
        {
            if (strChieuDai.length() == 0 || strChieuDai.contains("-"))
            {
                JOptionPane.showMessageDialog(rootPane, "Ban phai nhap vao gia tri chieu dai");
                txtChieuDai.requestFocus();
                return;
            }
            
            if (strChieuRong.length() == 0 || strChieuRong.contains("-"))
            {
                JOptionPane.showMessageDialog(rootPane, "Ban phai nhap vao gia tri chieu rong");
                txtChieuRong.requestFocus();
                return;
            }
            
            chieuDai = Double.parseDouble(strChieuDai);
            chieuRong = Double.parseDouble(strChieuRong);

            TinhToan tt = new TinhToan();

            strChuVi = tt.chuViHinhChuNhat(chieuDai, chieuRong) + "";
            strDienTich = tt.dienTichHinhChuNhat(chieuDai, chieuRong) + "";

            lblChuViHinhChuNhat.setText(strChuVi);
            lblDienTichHinhChuNhat.setText(strDienTich);
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(rootPane, "Ban can phai nhap gia tri kieu so. Chi tiet loi: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTinhToanHinhChuNhatActionPerformed

