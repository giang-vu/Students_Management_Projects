        strN = txtN.getText();
        
        try
        {
            if (strN.length() == 0 || strN.contains("-"))
            {
                JOptionPane.showMessageDialog(rootPane, "Ban phai nhap vao gia tri so tu nhien lon hon hoac bang 0");
                txtN.requestFocus();
                return;
            }
            
            n = Integer.parseInt(strN);
            
            TinhToan tt = new TinhToan();

            strGiaiThua = tt.tinhGiaiThua(n) + "";

            lblGiaiThua.setText(strGiaiThua);
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(rootPane, "Ban can phai nhap gia tri kieu so nguyen. Chi tiet loi: " + e.getMessage());
            txtN.requestFocus();
        }
