    private void btnTinhToanHinhThangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhToanHinhThangActionPerformed
        // TODO add your handling code here:
        String strDayLon = "", strDayBe = "", strChieuCao = "", strDienTich = "";
        double dayLon = 0, dayBe = 0, chieuCao = 0;
        
        strDayLon = txtDayLon.getText();
        strDayBe = txtDayBe.getText();
        strChieuCao = txtChieuCao.getText();
        
        try
        {
            if (strDayLon.length() == 0 || strDayLon.contains("-"))
            {
                JOptionPane.showMessageDialog(rootPane, "Ban phai nhap vao gia tri day lon hop le");
                txtDayLon.requestFocus();
                return;
            }
            
            if (strDayBe.length() == 0 || strDayBe.contains("-"))
            {
                JOptionPane.showMessageDialog(rootPane, "Ban phai nhap vao gia tri day be hop le");
                txtDayBe.requestFocus();
                return;
            }
            
            if (strChieuCao.length() == 0 || strChieuCao.contains("-"))
            {
                JOptionPane.showMessageDialog(rootPane, "Ban phai nhap vao gia tri chieu cao hop le");
                txtChieuCao.requestFocus();
                return;
            }

            dayLon = Double.parseDouble(strDayLon);
            dayBe = Double.parseDouble(strDayBe);
            
            if (dayLon < dayBe)
            {
                JOptionPane.showMessageDialog(rootPane, "Ban phai nhap vao day lon lon hon day be");
                txtDayLon.requestFocus();
                return;
            }
            
            chieuCao = Double.parseDouble(strChieuCao);

            TinhToan tt = new TinhToan();

            strDienTich = tt.dienTichHinhThang(dayLon, dayBe, chieuCao);

            lblDienTichHinhThang.setText(strDienTich);
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(rootPane, "Ban can phai nhap gia tri kieu so. Chi tiet loi: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTinhToanHinhThangActionPerformed

